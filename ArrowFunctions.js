// Single Parameter

const square = (x) => x * x;
console.log(square(4)); // 16

// Multiple Parameters

const multiply = (a, b) => a * b;
console.log(multiply(3, 5)); // 15

// No Parameters

const greet = () => "Hello, World!";
console.log(greet()); // "Hello, World!"

// Multiline Function

const sum = (a, b) => {
  const result = a + b;
  return result;
};
console.log(sum(10, 15)); // 25
